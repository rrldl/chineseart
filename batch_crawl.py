    def crawl_with_retry(self, query, entity_type, max_retries=3):
        """带重试的爬取"""
        for attempt in range(max_retries):
            try:
                print(f"爬取 [{entity_type}]: {query} (尝试 {attempt+1}/{max_retries})")
                result = self.service.crawl_encyclopedia_data(query, entity_type)
                
                if result:
                    self.success_count += 1
                    print(f"✓ 成功: {query}")
                    return True
                else:
                    print(f"✗ 失败: {query}")
                    
            except Exception as e:
                print(f"✗ 异常: {query} - {str(e)}")
            
            # 随机延迟，避免被反爬
            delay = random.uniform(2, 5)
            print(f"等待 {delay:.2f} 秒...")
            time.sleep(delay)
        
        self.failure_count += 1
        return False
    
